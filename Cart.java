package Models;

/**
 *
 * @author Shinpei
 */
public class Cart {

    private int id;
    private int acc_id;
    private int pro_id;
    private int quantity;
    private String note;

    public Cart() {
    }

    public Cart(int acc_id, int pro_id, int quantity, String note) {
        this.acc_id = acc_id;
        this.pro_id = pro_id;
        this.quantity = quantity;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAcc_id() {
        return acc_id;
    }

    public void setAcc_id(int acc_id) {
        this.acc_id = acc_id;
    }

    public int getPro_id() {
        return pro_id;
    }

    public void setPro_id(int pro_id) {
        this.pro_id = pro_id;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

}
